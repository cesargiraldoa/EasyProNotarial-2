export function getMunicipiosByDepartamento(nombreDepartamento: string): Municipio[] {
  const dep = DEPARTAMENTOS_COLOMBIA.find(
    d => d.nombre.toLowerCase() === nombreDepartamento.toLowerCase()
  )
  return dep?.municipios ?? []
}

export function getDepartamentoByMunicipio(nombreMunicipio: string): string | null {
  if (!nombreMunicipio) return null
  const lower = nombreMunicipio.toLowerCase().trim()
  for (const dep of DEPARTAMENTOS_COLOMBIA) {
    if (dep.municipios.some(m => m.nombre.toLowerCase() === lower)) {
      return dep.nombre
    }
  }
  return null
}
